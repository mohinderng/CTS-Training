using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace DataTypesExample
{
    class Program
    {
        static void Main(string[] args)
        {
            int a=5;
            float b=6.502f;
            double c=7.50345637;
            char d = 'D';
            uint e = 55;

            Console.WriteLine("The value of a = {0}", a);
            Console.WriteLine("The value of b = {0}", b);
            Console.WriteLine("The value of c = {0}", c);
            Console.WriteLine("The value of d = {0}", d);
            Console.WriteLine("The value of e = {0}", e);
        }
    }
}
